import fs from 'node:fs';
import path from 'node:path';

const root = path.resolve(import.meta.dirname, '..');
const buildVersion = process.env.BUILD_VERSION || makeBuildVersion();
const cacheVersion = `vts-${buildVersion.replace(/_/g, '-')}`;
const assetExtensions = new Set([
  '.css', '.html', '.js', '.json', '.manifest', '.png', '.jpg', '.jpeg', '.svg', '.webp'
]);
const assetDirs = ['css', 'js', 'images', 'assets', 'tabs', 'workers'];
const rootFiles = ['/', '/index.html', '/admin.html', '/CNAME', '/robots.txt', '/sitemap.xml', '/site.webmanifest', '/404.html'];

function makeBuildVersion() {
  const d = new Date();
  const pad = (n) => String(n).padStart(2, '0');
  return [
    d.getUTCFullYear(),
    pad(d.getUTCMonth() + 1),
    pad(d.getUTCDate()),
    '_',
    pad(d.getUTCHours()),
    pad(d.getUTCMinutes()),
    pad(d.getUTCSeconds()),
  ].join('');
}

function readText(file) {
  return fs.readFileSync(path.join(root, file), 'utf8');
}

function writeText(file, text) {
  fs.writeFileSync(path.join(root, file), text);
}

function updateCacheBusters() {
  for (const file of ['index.html', 'admin.html']) {
    if (!fs.existsSync(path.join(root, file))) continue;
    const html = readText(file)
      .replace(/\?v=[0-9A-Za-z_-]+/g, `?v=${buildVersion}`)
      .replace(/(src="js\/(?:app|admin-page)\.js)(?:\?v=[0-9A-Za-z_-]+)?"/g, `$1?v=${buildVersion}"`);
    writeText(file, html);
  }

  const appPath = path.join(root, 'js', 'app.js');
  if (fs.existsSync(appPath)) {
    const app = fs.readFileSync(appPath, 'utf8')
      .replace(/app-loading\.js\?v=[0-9A-Za-z_-]+/g, `app-loading.js?v=${buildVersion}`);
    fs.writeFileSync(appPath, app);
  }

  const adminPagePath = path.join(root, 'js', 'admin-page.js');
  if (fs.existsSync(adminPagePath)) {
    const adminPage = fs.readFileSync(adminPagePath, 'utf8')
      .replace(/ocr-dashboard\.js(?:\?v=[0-9A-Za-z_-]+)?/g, `ocr-dashboard.js?v=${buildVersion}`);
    fs.writeFileSync(adminPagePath, adminPage);
  }
}

function walkAssets(dir, urls) {
  const absDir = path.join(root, dir);
  if (!fs.existsSync(absDir)) return;
  for (const entry of fs.readdirSync(absDir, { withFileTypes: true })) {
    const abs = path.join(absDir, entry.name);
    const rel = path.relative(root, abs).replace(/\\/g, '/');
    if (entry.isDirectory()) {
      walkAssets(rel, urls);
      continue;
    }
    const ext = path.extname(entry.name).toLowerCase();
    if (!assetExtensions.has(ext)) continue;
    urls.add(`/${rel}`);
  }
}

function buildPrecacheUrls() {
  const urls = new Set(rootFiles);
  if (fs.existsSync(path.join(root, 'public', '404.html'))) urls.add('/404.html');
  for (const dir of assetDirs) walkAssets(dir, urls);
  urls.delete('/js/eden-datasets.payload.js.map');
  return [...urls].sort((a, b) => a.localeCompare(b));
}

function writeServiceWorker() {
  const urls = buildPrecacheUrls();
  const manifest = urls.map((url) => `  '${url}'`).join(',\n');
  const sw = `// Generated by scripts/update-build-metadata.mjs.
const CACHE_VERSION = '${cacheVersion}';
const APP_SHELL = [
${manifest}
];

function normalizedRequestKey(request) {
  const url = new URL(request.url);
  return url.origin === self.location.origin ? url.pathname : request;
}

self.addEventListener('install', (event) => {
  self.skipWaiting();
  event.waitUntil(
    caches.open(CACHE_VERSION).then((cache) =>
      Promise.allSettled(APP_SHELL.map((url) => cache.add(url)))
    )
  );
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys()
      .then((keys) => Promise.all(keys.filter((key) => key !== CACHE_VERSION).map((key) => caches.delete(key))))
      .then(() => self.clients.claim())
  );
});

self.addEventListener('fetch', (event) => {
  const { request } = event;
  const url = new URL(request.url);

  if (request.method !== 'GET' || url.origin !== self.location.origin) return;

  const cacheKey = normalizedRequestKey(request);
  const shouldNetworkFirst =
    request.destination === 'script' ||
    request.destination === 'style' ||
    url.pathname === '/index.html' ||
    url.pathname.startsWith('/js/') ||
    url.pathname.startsWith('/css/');

  if (request.mode === 'navigate') {
    event.respondWith(
      fetch(new Request(request, { cache: 'reload' }))
        .then((response) => {
          const copy = response.clone();
          caches.open(CACHE_VERSION).then((cache) => cache.put('/index.html', copy));
          return response;
        })
        .catch(() => caches.match('/index.html'))
    );
    return;
  }

  if (shouldNetworkFirst) {
    event.respondWith(
      fetch(new Request(request, { cache: 'reload' }))
        .then((response) => {
          if (response.ok) {
            const copy = response.clone();
            caches.open(CACHE_VERSION).then((cache) => cache.put(cacheKey, copy));
          }
          return response;
        })
        .catch(() => caches.match(cacheKey).then((cached) => cached || caches.match(request)))
    );
    return;
  }

  event.respondWith(
    caches.match(request).then((cached) => {
      const network = fetch(request).then((response) => {
        if (response.ok) {
          const copy = response.clone();
          caches.open(CACHE_VERSION).then((cache) => cache.put(request, copy));
        }
        return response;
      });
      return cached || network;
    })
  );
});
`;
  writeText('public/sw.js', sw);
  console.log(`Updated build metadata ${buildVersion}; service worker precaches ${urls.length} URLs.`);
}

updateCacheBusters();
writeServiceWorker();
